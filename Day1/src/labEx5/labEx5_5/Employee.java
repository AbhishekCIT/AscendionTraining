package labEx5.labEx5_5;

public class Employee {
    private int empno;
    private String name;

    public Employee(int empno, String name) {
        this.empno = empno;
        this.name = name;
    }

    public int getEmpno() {
        return empno;
    }

    public String getName() {
        return name;
    }
}


