package calc;

public class Addition {
    void add(int a,int b){
        System.out.println(a+b);
    }
    void add(int a,int b, int c){
        System.out.println(a+b+c);
    }
    void add(double a,double b){
        System.out.println(a+b);
    }
    void add(String a,int b){
        System.out.println(a+" "+b);
    }
}
